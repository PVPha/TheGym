     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        DB::table('class_registration')->insert([
            'class_id' => $request->class_id,
            'member_id' => $request->id
        ]);
        return redirect('/user');
    }

    /**
     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        $data = DB::table('class_registration')->join('class', 'class_registration.class_id', '=', 'class.class_id')->join('users', 'class_registration.member_id', '=', 'users.id')->where('class_registration.member_id', '=', $id)->get(['*', 'class_registration.id']);
        return $data;
    }

